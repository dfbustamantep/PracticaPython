# Operadores

# Operadores aritmeticos
print(3 + 4)
print(3 - 4)
print(3 * 4)
print(3 / 4)
print(10 % 2)
# Nos da siempre un numero entero
print(10 // 3)
# Exponente
print(2 ** 3)

print("Hola "+"python")
print("Hola "+str(5))
print("Hola "*5)

# Operadores comparativos
print(3>4)
print(3<4)
print(3>=4)
print(3<=4)
print(3==4)
print(3!=4)


print('Hola'>'Python')
print('Hola'<'Python')
print('Hola'>='Python')
print('Hola'<='Python')
print('Hola'=='Python')
print('Hola'!='Python')

# Operadores Logicos
print(3>4 and 'Hola'>'Python')
print(3>4 or 'Hola'>'Python')
print(3<4 and 'Hola'<'Python')
print(3<4 or 'Hola'<'Python')
print(not(3>4))
